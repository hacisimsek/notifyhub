    LanguageMessagesResponse messages(String requestedLanguage) {
        String language = normalize(requestedLanguage);
        return new LanguageMessagesResponse(language, TR.equals(language) ? TR_MESSAGES : EN_MESSAGES);
    }

    private String normalize(String language) {
        if (language == null) {
            return EN;
        }
        String normalized = language.trim().toLowerCase();
        return normalized.startsWith(TR) ? TR : EN;
    }
